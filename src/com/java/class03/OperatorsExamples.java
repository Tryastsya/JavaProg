package com.java.class03;

public class OperatorsExamples {
    public static void main(String args[]){
        int a=10, b=20;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(b/a);
        System.out.println(b%a);
    }
}
