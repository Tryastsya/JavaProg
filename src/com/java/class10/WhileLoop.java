package com.java.class10;

public class WhileLoop {
    public static void main(String[] args) {

// while(condition){executing part} then checking condition again

        int a = 1;
        while(a < 6) {
            System.out.println("Hello");
            a++;

        }
        a = 0;
        while (a<50){
            a++;
            System.out.println(a);
        }
    }
}
