package com.java.class09;

public class IncrementOperators {
    public static void main(String[] args) {
        int i = 5;                          //5
        i = i + 1; // ++ or -- = +1 or -1   //6
        System.out.println(i);
        i++;                                //7
        System.out.println(i);
        i--;                                //6
        System.out.println(i);
    }
}
