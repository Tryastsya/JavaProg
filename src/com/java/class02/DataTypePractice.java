package com.java.class02;

public class DataTypePractice {

    public static void main(String[] args) {
        byte num1 = 127;
        short num2 = 32767;
        int num3 = 88000000;
        long num4 = 12345678901234l;
        float num5 = 45645.458f;
        double num6 = 123456.78444487d;
        String num7 = "This is my Java program!";
        char num8 = 'a';
        boolean num9 = false;

        System.out.println("Byte is " + num1);
        System.out.println("Short is " + num2);
        System.out.println("Int is " + num3);
        System.out.println("Long is " + num4);
        System.out.println("Float is " + num5);
        System.out.println("Double is " + num6);
        System.out.println(num7);
        System.out.println(num8);
        System.out.println(num9);


    }
}
